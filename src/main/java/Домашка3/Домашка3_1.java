package Домашка3;


public class Домашка3_1 {
    public static void main(String[] args) {
        invertArray();
        System.out.println();

    }

    public static void invertArray() {
        int[] arr = {1, 0, 1, 0, 0, 1};
        for (int i = 0; i < arr.length; i++) {
            System.out.println(arr[i] - 1);

        }

    }
}





